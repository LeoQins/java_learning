// VariablesExample.java - 演示Java的基本变量类型

public class VariablesExample {
    public static void main(String[] args) {
        // 整数类型
        int age = 25;
        System.out.println("年龄 (int): " + age);

        // 浮点数类型
        double price = 99.99;
        System.out.println("价格 (double): " + price);

        // 字符类型
        char grade = 'A';
        System.out.println("等级 (char): " + grade);

        // 布尔类型
        boolean isJavaFun = true;
        System.out.println("Java有趣吗? (boolean): " + isJavaFun);

        // 字符串类型 (这是一个对象，不是基本类型)
        String greeting = "Hello, Java!";
        System.out.println("问候语 (String): " + greeting);
    }
}
